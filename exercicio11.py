#11-crie uma lista e adicione um item novo a ela utilizando a metodolofia LIFO

# Criando uma lista LIFO
lifo = [1, 2, 3, 4, 5]
lifo.append(6)

print(lifo)
# usei o método append(6) para adicionar um item à lista, que segue a metodologia LIFO, O último item adicionado será o primeiro a ser
# removido quando a operação de pilha for aplicada.
