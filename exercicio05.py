#5-Retire o segundo item dessa lista e imprima ela:

lista = [1,2,3,4,5,6]

# retira o segundo item usando a função pop, através do indice(1)
lista.pop(1)

# imprime na tela a lista atualizada
print(lista)

# usei o comando lista.pop para remover o item na posição 1, que corresponde ao segundo item da lista.
# esta abordagem é eficiente e direta para a modificação da lista.